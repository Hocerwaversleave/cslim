import Foundation

extension ValidReturnTypes {

    func methodReturningSwiftString() -> String {
        return "Hello Swift"
    }
    
    func methodReturningSwiftBoolTrue() -> Bool {
        return true
    }
    
    func methodReturningSwiftBoolFalse() -> Bool {
        return false
    }
    
    func methodReturningSwiftInt() -> Int {
        return 789
    }
    
    func methodReturningSwiftDouble() -> Double {
        return 123.45
    }
    
    func methodReturningSwiftFloat() -> Float {
        return  3.14159
    }
}
